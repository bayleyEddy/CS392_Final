using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using CS392_Demo3.Data;
using CS392_Demo3.Models;

namespace CS392_Demo3.Pages.departmentPages
{
    public class DetailsModel : PageModel
    {
        private readonly CS392_Demo3.Data.SchoolDbContext _context;

        public DetailsModel(CS392_Demo3.Data.SchoolDbContext context)
        {
            _context = context;
        }

        public Department Department { get; set; } = default!;

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            var department = await _context.Departments.FirstOrDefaultAsync(m => m.DNumber == id);
            if (department == null)
            {
                return NotFound();
            }
            else
            {
                Department = department;
            }
            return Page();
        }
    }
}
